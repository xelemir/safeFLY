//
//  CountryBoundaries.swift
//  safeFLY
//
//  Real country borders used to gate provider work to the country a tap or map
//  actually falls in. Each provider only owns drone data for one country, so a
//  point-in-polygon test against the real outline lets a provider skip its network
//  query (WMS GetFeatureInfo) entirely when the coordinate is elsewhere, instead of
//  firing a request that a loose bounding box could not rule out.
//
//  Source: Natural Earth-derived national outlines (georgique/world-geojson),
//  clipped to mainland Europe, buffered outward ~2 km so points just inside a
//  border are never wrongly excluded, then simplified to ~3 km. Generated, not
//  hand-drawn. Coordinates are [longitude, latitude].
//

import Foundation

// A country's real outline as one or more disjoint polygons (mainland plus islands).
// `contains` does a cheap bounding-box reject before the ray-cast point-in-polygon.
struct CountryCoverage: Sendable {
    let boundingBox: BoundingBox
    let polygons: [[[Double]]]

    nonisolated func contains(_ coordinate: MapCoordinate) -> Bool {
        guard boundingBox.contains(coordinate) else { return false }
        for polygon in polygons where GeoMath.contains(coordinate, polygon: polygon) {
            return true
        }
        return false
    }

    // Whether any of this country's territory is visible in `region`, used to decide which
    // providers' overlays to render. A center-in-country test alone fails the common border
    // view (e.g. 60% Germany / 40% Switzerland) where the center sits in one country while a
    // neighbour fills the rest of the screen — both should render. We treat the country as
    // visible when its real outline overlaps the viewport rectangle.
    nonisolated func intersects(_ region: MapRegion) -> Bool {
        guard boundingBox.intersects(region) else { return false }

        let minLat = region.center.latitude - region.latitudeDelta / 2
        let maxLat = region.center.latitude + region.latitudeDelta / 2
        let minLon = region.center.longitude - region.longitudeDelta / 2
        let maxLon = region.center.longitude + region.longitudeDelta / 2

        // Any viewport corner (or its center) sitting on this country's territory.
        let probes = [
            region.center,
            MapCoordinate(latitude: minLat, longitude: minLon),
            MapCoordinate(latitude: minLat, longitude: maxLon),
            MapCoordinate(latitude: maxLat, longitude: minLon),
            MapCoordinate(latitude: maxLat, longitude: maxLon)
        ]
        for probe in probes where contains(probe) {
            return true
        }

        // Or any outline vertex falling inside the viewport — catches a small country fully
        // contained in a zoomed-out view, where no corner happens to land on its territory.
        for polygon in polygons {
            for point in polygon where point.count >= 2 {
                if point[1] >= minLat && point[1] <= maxLat &&
                    point[0] >= minLon && point[0] <= maxLon {
                    return true
                }
            }
        }
        return false
    }
}

extension CountryCoverage {
    // Merges several country outlines into one coverage: the union of their polygons and a
    // bounding box spanning all of them. Used by cross-border providers such as the EU
    // protected-areas overlay, which serves more than one country from a single source.
    nonisolated static func union(_ coverages: [CountryCoverage]) -> CountryCoverage {
        let boxes = coverages.map(\.boundingBox)
        let box = BoundingBox(
            minLat: boxes.map(\.minLat).min() ?? 0,
            maxLat: boxes.map(\.maxLat).max() ?? 0,
            minLon: boxes.map(\.minLon).min() ?? 0,
            maxLon: boxes.map(\.maxLon).max() ?? 0
        )
        return CountryCoverage(boundingBox: box, polygons: coverages.flatMap(\.polygons))
    }
}

