//
//  CountryBoundaries.swift
//  safeFLY
//
//  Real country borders used to gate provider work to the country a tap or map
//  actually falls in. Each provider only owns drone data for one country, so a
//  point-in-polygon test against the real outline lets a provider skip its network
//  query (WMS GetFeatureInfo) entirely when the coordinate is elsewhere, instead of
//  firing a request that a loose bounding box could not rule out.
//
//  Source: Natural Earth-derived national outlines (georgique/world-geojson),
//  clipped to mainland Europe, buffered outward ~2 km so points just inside a
//  border are never wrongly excluded, then simplified to ~3 km. Generated, not
//  hand-drawn. Coordinates are [longitude, latitude].
//

import Foundation

// A country's real outline as one or more disjoint polygons (mainland plus islands).
// `contains` does a cheap bounding-box reject before the ray-cast point-in-polygon.
struct CountryCoverage: Sendable {
    let boundingBox: BoundingBox
    let polygons: [[[Double]]]

    nonisolated func contains(_ coordinate: MapCoordinate) -> Bool {
        guard boundingBox.contains(coordinate) else { return false }
        for polygon in polygons where GeoMath.contains(coordinate, polygon: polygon) {
            return true
        }
        return false
    }
}

